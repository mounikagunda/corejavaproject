// program on indexOf
package strings;

public class Stringex17
{
	public static void main(String[] args) 
	{
		  
		   String str = "dhatri info solutions";
		    
		   // returns the index of occurrence of character d
		   System.out.println("index of letter d = "+ str.indexOf('d')); 
		      
		   // returns -1 if character g is not in the string
		   System.out.println("index of letter g = "+ str.indexOf('g'));
		   }

}
